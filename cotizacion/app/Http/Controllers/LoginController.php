    public function login()
    {
        $credentials = request()->only('email','password');

        if(Auth::attempt($credentials)){
            $user = Auth::user(); 
            $unit_name = $user->unit->name;
            //dd($unit_name);
            return redirect()->route('unidades.index', ['name' => $unit_name]);
        }

        return redirect('login');
    }

